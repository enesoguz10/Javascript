// String To Numbers

let a = 5;
let b = ("10");
console.log(a + b);

let c = Number(b);
console.log(typeof c);
console.log(a + c);

// parseFloat

let d = 7;
let e = parseFloat("13.7");
console.log(typeof e);
console.log(e);

// Numbers To String

let x = String(99);
console.log(typeof x);
console.log(x);

let y = (55).toString();
console.log(typeof y);
console.log(y);

// Boolean To String

let sonuc = String(true);
console.log(typeof sonuc);
console.log(sonuc);

// Object

let rakam = [1,2,3,4,5];
console.log(typeof rakam);
console.log(rakam);

// Object To String
let rakamlar = String([0, 1, 2, 3]);
console.log(typeof rakamlar);
console.log(rakamlar);